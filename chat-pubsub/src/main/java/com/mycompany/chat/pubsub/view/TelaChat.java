    private void btnSairTopicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSairTopicoActionPerformed
        String topico = getTopicoSelecionado();

        if (topico == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um topico para sair.");
            return;
        }

        int opcao = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Deseja sair do tópico \"" + topico + "\"?",
                "Confirmar saída",
                javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (opcao != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }

        cliente.sairTopico(topico);

        if (topico.equals(topicoAberto)) {
            topicoAberto = null;

            jScrollPane2.setVisible(false);
            txtMensagem.setVisible(false);
            btnEnviar.setVisible(false);
            btnVoltar.setVisible(false);
            lblTopicoAtual.setVisible(false);
            btnSairTopico.setVisible(false);
            btnSair.setVisible(true);
            lblParticipantes.setVisible(false);
            listParticipantes.setVisible(false);
        }
    }//GEN-LAST:event_btnSairTopicoActionPerformed

