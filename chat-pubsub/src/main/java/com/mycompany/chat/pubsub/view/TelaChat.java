    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        String mensagem = txtMensagem.getText().trim();
        if (mensagem.isEmpty()) {
            return;
        }
        if (topicoAberto == null) {
            JOptionPane.showMessageDialog(this, "Abra um topico antes de enviar mensagens.");
            return;
        }
        if (!cliente.possuiChaveTopico(topicoAberto)) {
            JOptionPane.showMessageDialog(this, "vc nao tem chave do topico");
            return;
        }
        try {
            String chaveTopico = cliente.getChaveTopico(topicoAberto);
            Integer versao = cliente.getVersaoChaveTopico(chaveTopico);
            String mensagemCriptografada = CryptoUtil.criptografarAES(mensagem, chaveTopico);

            cliente.enviarMensagemCriptografada(mensagemCriptografada, versao);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "erro p criptografar");
            e.printStackTrace();
        }

        System.out.println("Topico aberto: " + topicoAberto);
        System.out.println("Possui chave? " + cliente.possuiChaveTopico(topicoAberto));
        System.out.println("Chave: " + cliente.getChaveTopico(topicoAberto));
        System.out.println("Versao: " + cliente.getVersaoChaveTopico(topicoAberto));
        txtMensagem.setText(" ");
    }//GEN-LAST:event_btnEnviarActionPerformed

